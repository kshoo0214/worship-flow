let currentLang = 'ko';

function setLanguage(lang) {
  currentLang = STRINGS[lang] ? lang : 'ko';
}

function t(key, vars = {}) {
  let s = STRINGS[currentLang]?.[key] ?? STRINGS.ko[key] ?? key;
  Object.entries(vars).forEach(([k, v]) => {
    s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  });
  return s;
}

function getLanguage() {
  return currentLang;
}

